function somar(a, b) {
    return a + b;
}

function subtrair(a, b){
    return a - b;
}

function multiplicar(a, b){
    return a * b;
}

function dividir(a, b){
    return a / b;
}

function potencia(a){
    return a * a;
}

function raiz(a){
    return Math.sqrt(a);
}

function fracao(a){
    return 1 / a;
}

function porcentagem(a, b){
    return (a * (b / 100));
}
